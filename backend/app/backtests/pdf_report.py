def _page_decoration(canvas: Any, document: Any) -> None:
    """Footer with the page number on every page."""
    canvas.saveState()
    canvas.setFont(_LATIN_FONT, 7.5)
    canvas.setFillColor(colors.HexColor("#777777"))
    canvas.drawCentredString(_PAGE_SIZE[0] / 2, _MARGIN * 0.55, f"Page {canvas.getPageNumber()}")
    canvas.drawRightString(_PAGE_SIZE[0] - _MARGIN, _MARGIN * 0.55, "Grid Backtester")
    canvas.restoreState()


