def build_backtest_pdf_report(session: Session, *, run: BacktestRun) -> bytes:
    """Render one owned BacktestRun to PDF bytes.

    Ownership must already be resolved by the caller. Read-only: the two
    series queries are ordered projections and nothing is written, flushed,
    or committed.
    """
    _ensure_fonts()
    styles = _Styles()

    dataset = run.dataset
    configuration = _mapping(run.configuration)
    metrics_document = _mapping(run.result_metrics)
    has_result = run.result_metrics is not None
    inner = _mapping(metrics_document.get("metrics"))
    strategy = _mapping(inner.get("strategy"))
    trade_costs = _mapping(inner.get("trade_costs"))
    zones = _mapping(inner.get("zones"))
    first_return = _mapping(inner.get("first_return"))

    daily_rows = load_daily_equity_projection(session, backtest_run_id=run.id)
    trades = load_trade_projection(session, backtest_run_id=run.id, limit=FIRST_TRADE_ROWS)

    story: list[Any] = []

    # 1. Project/backtest name -------------------------------------------------
    story.append(styles.para(run.name, styles.title, styles.title_cjk))
    story.append(
        Paragraph(
            f"Backtest #{run.id} &middot; Status {run.status} &middot; "
            f"Created {_timestamp_text(run.created_at)} &middot; "
            f"Completed {_timestamp_text(run.completed_at)}",
            styles.subtitle,
        )
    )

    # 2/3/4. Security information, data range, cleaning summary ---------------
    story.append(Paragraph("Security and Dataset", styles.heading))
    story.append(
        _key_value_table(
            styles,
            [
                ("Security Name", _dash(dataset.security_name)),
                ("Security Code", _dash(dataset.security_code)),
                ("Dataset Name", dataset.name),
                ("Source Type", dataset.source_type),
                ("Original Filename", dataset.original_filename),
                ("Data Mode", dataset.data_mode),
                (
                    "Dataset Range",
                    f"{_date_text(dataset.start_date)} to {_date_text(dataset.end_date)}",
                ),
                ("Backtest Range", f"{_date_text(run.start_date)} to {_date_text(run.end_date)}"),
                ("Row Count", _int_text(dataset.row_count)),
            ],
        )
    )

    story.append(Paragraph("Data-Cleaning Summary", styles.heading))
    cleaning = _mapping(dataset.cleaning_summary)
    if cleaning:
        story.append(
            _key_value_table(
                styles,
                [
                    (str(key), _dash(value) if not isinstance(value, dict | list) else str(value))
                    for key, value in cleaning.items()
                ],
            )
        )
    else:
        story.append(styles.body_para("No cleaning summary recorded for this dataset."))

    # 5. Strategy parameters ---------------------------------------------------
    story.append(Paragraph("Strategy Parameters", styles.heading))
    story.append(
        _key_value_table(
            styles,
            [
                ("Initial Cash", _money(configuration.get("initial_cash"))),
                ("Initial Shares", _int_text(configuration.get("initial_shares"))),
                ("Lot Size", _int_text(configuration.get("lot_size"))),
                ("Trade Lots", _int_text(configuration.get("trade_lots"))),
                (
                    "Baseline",
                    _money(configuration.get("baseline"))
                    if configuration.get("baseline") is not None
                    else "- (default: first Close)",
                ),
                ("A Distance", _value_config(configuration.get("a_distance"))),
                ("C Distance", _value_config(configuration.get("c_distance"))),
                ("Grid Step", _value_config(configuration.get("grid_step"))),
                (
                    "Tick Size",
                    f"{_enabled(_mapping(configuration.get('tick_size')).get('enabled'))}"
                    f" @ {_money(_mapping(configuration.get('tick_size')).get('value'))}",
                ),
                ("Risk-Free Rate (annual)", _ratio(configuration.get("risk_free_rate_annual"))),
            ],
        )
    )

    if has_result:
        story.append(Paragraph("Baseline and Grid Geometry", styles.heading))
        grid_levels = metrics_document.get("grid_levels")
        level_count = len(grid_levels) if isinstance(grid_levels, list) else 0
        story.append(
            _key_value_table(
                styles,
                [
                    ("Baseline", _money(metrics_document.get("baseline"))),
                    ("A Zone Lower", _money(metrics_document.get("a_lower"))),
                    ("A Zone Upper", _money(metrics_document.get("a_upper"))),
                    ("C Zone Lower", _money(metrics_document.get("c_lower"))),
                    ("C Zone Upper", _money(metrics_document.get("c_upper"))),
                    ("Grid Step Size", _money(metrics_document.get("grid_step"))),
                    ("Grid Level Count", _int_text(level_count)),
                ],
            )
        )

    # 6. Fee and slippage assumptions -----------------------------------------
    story.append(Paragraph("Fee and Slippage Assumptions", styles.heading))
    story.append(
        _key_value_table(
            styles,
            [
                ("Buy Commission", _commission_text(configuration.get("buy_commission"))),
                ("Sell Commission", _commission_text(configuration.get("sell_commission"))),
                ("Slippage", _slippage_text(configuration.get("slippage"))),
            ],
        )
    )

    # 7. OHLC-path assumption --------------------------------------------------
    story.append(Paragraph("OHLC-Path Assumption", styles.heading))
    if dataset.data_mode == "CLOSE_ONLY":
        story.append(
            styles.body_para(
                "Data mode is CLOSE_ONLY: the dataset carries no intraday high/low, "
                "so no OHLC path was reconstructed and the configured path mode "
                f"({_dash(configuration.get('ohlc_path_mode'))}) was not applied."
            )
        )
    else:
        story.append(
            _key_value_table(
                styles,
                [
                    ("Configured Path Mode", _dash(configuration.get("ohlc_path_mode"))),
                    ("Stored Path Mode", _dash(run.ohlc_path_mode)),
                ],
            )
        )

    # 8. Core metrics ----------------------------------------------------------
    story.append(Paragraph("Core Metrics", styles.heading))
    if has_result:
        final_state = _mapping(metrics_document.get("final_state"))
        story.append(
            _key_value_table(
                styles,
                [
                    ("Initial Equity", _money(strategy.get("initial_equity"))),
                    ("Final Equity", _money(strategy.get("final_equity"))),
                    ("Net Profit", _money(strategy.get("net_profit"))),
                    ("Total Return", _ratio(strategy.get("total_return"))),
                    ("Annualized Return", _ratio(strategy.get("annualized_return"))),
                    ("Maximum Drawdown", _ratio(strategy.get("maximum_drawdown"))),
                    ("Sharpe Ratio", _quantize(strategy.get("sharpe_ratio"))),
                    ("First Return Equity", _money(first_return.get("equity"))),
                    ("Days Until First Return", _int_text(first_return.get("days"))),
                    ("Final Cash", _money(final_state.get("cash"))),
                    ("Final Shares", _int_text(final_state.get("shares"))),
                    ("Final Zone State", _dash(final_state.get("zone_state"))),
                ],
            )
        )

        story.append(Paragraph("Trade and Zone Statistics", styles.heading))
        zone_counts = _mapping(zones.get("zone_event_counts"))
        story.append(
            _key_value_table(
                styles,
                [
                    ("Executed Trades", _int_text(trade_costs.get("executed_trades"))),
                    ("Skipped Trades", _int_text(trade_costs.get("skipped_trades"))),
                    ("BUY Count", _int_text(trade_costs.get("buy_count"))),
                    ("SELL Count", _int_text(trade_costs.get("sell_count"))),
                    ("Days Closed in A Zone", _int_text(zones.get("days_closed_in_a_zone"))),
                    ("Days Closed in C Zone", _int_text(zones.get("days_closed_in_c_zone"))),
                    ("Days Closed Outside C", _int_text(zones.get("days_closed_outside_c"))),
                    *[
                        (f"Zone Event: {name}", _int_text(count))
                        for name, count in zone_counts.items()
                    ],
                ],
            )
        )
    else:
        story.append(
            styles.body_para("Result data is unavailable for this run, so no metrics are shown.")
        )

    # 9. Both Buy-and-Hold benchmarks -----------------------------------------
    story.append(Paragraph("Buy-and-Hold Benchmarks", styles.heading))
    if has_result:
        benchmark_rows = [
            *_equity_series_rows("Benchmark 1", _mapping(inner.get("benchmark1"))),
            *_equity_series_rows("Benchmark 2", _mapping(inner.get("benchmark2"))),
        ]
        story.append(_key_value_table(styles, benchmark_rows))

        day_one = _mapping(_mapping(metrics_document.get("benchmark2")).get("day_one_purchase"))
        story.append(Paragraph("Benchmark 2 Day-One Purchase", styles.heading))
        if day_one:
            story.append(
                _key_value_table(
                    styles,
                    [
                        ("Reference Price", _money(day_one.get("reference_price"))),
                        ("Tick Price", _money(day_one.get("tick_price"))),
                        ("Execution Price", _money(day_one.get("execution_price"))),
                        ("Lots", _int_text(day_one.get("lots"))),
                        ("Shares Purchased", _int_text(day_one.get("shares_purchased"))),
                        ("Notional", _money(day_one.get("notional"))),
                        ("Commission", _money(day_one.get("commission"))),
                        ("Slippage Cost", _money(day_one.get("slippage_cost"))),
                        ("Cash After", _money(day_one.get("cash_after"))),
                        ("Shares After", _int_text(day_one.get("shares_after"))),
                    ],
                )
            )
        else:
            story.append(styles.body_para("No day-one purchase was recorded."))
    else:
        story.append(styles.body_para("Benchmark data is unavailable for this run."))

    # 10/11/12. Charts ---------------------------------------------------------
    labels = [row.date.isoformat() for row in daily_rows]

    story.append(Paragraph("Price and Grid Geometry", styles.heading))
    if daily_rows:
        reference_lines: list[tuple[str, Decimal, colors.Color]] = []
        faint: list[Decimal] = []
        if has_result:
            for label, key, color in (
                ("Baseline", "baseline", _STRATEGY_COLOR),
                ("A Lower", "a_lower", _BENCHMARK2_COLOR),
                ("A Upper", "a_upper", _BENCHMARK2_COLOR),
                ("C Lower", "c_lower", _BENCHMARK1_COLOR),
                ("C Upper", "c_upper", _BENCHMARK1_COLOR),
            ):
                parsed = _decimal(metrics_document.get(key))
                if parsed is not None:
                    reference_lines.append((label, parsed, color))
            levels = metrics_document.get("grid_levels")
            if isinstance(levels, list):
                faint = [
                    parsed for parsed in (_decimal(level) for level in levels) if parsed is not None
                ]
        story.append(
            _line_chart(
                title="Daily Close with Baseline, A/C Boundaries and Grid Levels",
                labels=labels,
                series=[("Close", [row.close for row in daily_rows], _STRATEGY_COLOR, None)],
                reference_lines=reference_lines,
                faint_lines=faint,
            )
        )
    else:
        story.append(styles.body_para("No daily price series is available for this run."))

    story.append(Spacer(1, 10))
    story.append(Paragraph("Equity Curve", styles.heading))
    if daily_rows:
        story.append(
            _line_chart(
                title="Daily Close Equity vs Buy-and-Hold Benchmarks",
                labels=labels,
                series=[
                    ("Strategy", [row.equity for row in daily_rows], _STRATEGY_COLOR, None),
                    (
                        "Benchmark 1",
                        _benchmark_points(metrics_document, "benchmark1"),
                        _BENCHMARK1_COLOR,
                        (4, 2),
                    ),
                    (
                        "Benchmark 2",
                        _benchmark_points(metrics_document, "benchmark2"),
                        _BENCHMARK2_COLOR,
                        (1, 2),
                    ),
                ],
            )
        )
    else:
        story.append(styles.body_para("No equity series is available for this run."))

    story.append(Spacer(1, 10))
    story.append(Paragraph("Drawdown", styles.heading))
    if daily_rows:
        story.append(
            _zero_reference_chart(
                title="Daily Drawdown",
                labels=labels,
                values=[row.drawdown for row in daily_rows],
            )
        )
    else:
        story.append(styles.body_para("No drawdown series is available for this run."))

    # 13. First 20 trades ------------------------------------------------------
    story.append(Paragraph(f"First {FIRST_TRADE_ROWS} Trades", styles.heading))
    if trades:
        rows = [
            [
                str(sequence),
                event_date.isoformat(),
                trade.side,
                _money(trade.grid_price),
                _money(trade.execution_price),
                _int_text(trade.shares),
                _money(trade.notional),
                _money(trade.commission),
                _money(trade.equity_after),
                trade.status,
                _dash(trade.skip_reason),
            ]
            for trade, event_date, sequence in trades
        ]
        story.append(
            _grid_table(
                styles,
                [
                    "Seq",
                    "Date",
                    "Side",
                    "Grid",
                    "Exec",
                    "Shares",
                    "Notional",
                    "Comm",
                    "Equity",
                    "Status",
                    "Skip",
                ],
                rows,
                # Skip reasons are long unbroken tokens, so that column gets
                # the widest share after Equity.
                weights=[3.5, 8.5, 4.5, 8.0, 8.0, 6.0, 8.5, 7.5, 11.0, 8.0, 12.0],
            )
        )
        story.append(
            Paragraph(
                f"Showing the first {len(rows)} trade(s) ordered by event sequence. "
                "The complete trade history is available from trades.csv.",
                styles.note,
            )
        )
    else:
        story.append(styles.body_para("This run recorded no trades."))

    # 14. Cost summary ---------------------------------------------------------
    story.append(Paragraph("Cost Summary", styles.heading))
    if has_result:
        story.append(
            _key_value_table(
                styles,
                [
                    ("Total Commission", _money(trade_costs.get("total_commission"))),
                    ("Total Slippage Cost", _money(trade_costs.get("total_slippage_cost"))),
                    (
                        "Benchmark 2 Day-One Commission",
                        _money(inner.get("benchmark2_day_one_commission")),
                    ),
                    (
                        "Benchmark 2 Day-One Slippage Cost",
                        _money(inner.get("benchmark2_day_one_slippage_cost")),
                    ),
                ],
            )
        )
    else:
        story.append(styles.body_para("Cost data is unavailable for this run."))

    # Failure section ----------------------------------------------------------
    if run.status != "COMPLETED":
        story.append(Paragraph(f"Run Status: {run.status}", styles.heading))
        story.append(
            _key_value_table(
                styles,
                [
                    ("Status", run.status),
                    ("Error Message", _dash(run.error_message)),
                    (
                        "Result Data",
                        "Unavailable - this run produced no stored result document."
                        if not has_result
                        else "Partial result data is stored for this run.",
                    ),
                ],
            )
        )
        story.append(
            Paragraph(
                "No metrics, benchmarks, or charts are synthesized for a run that did "
                "not complete successfully.",
                styles.note,
            )
        )

    # 15. Risk disclaimer ------------------------------------------------------
    story.append(Spacer(1, 12))
    story.append(
        KeepTogether(
            [
                Paragraph("Risk Disclaimer", styles.heading),
                Paragraph(RISK_DISCLAIMER, styles.disclaimer),
            ]
        )
    )

    buffer = io.BytesIO()
    document = SimpleDocTemplate(
        buffer,
        pagesize=_PAGE_SIZE,
        leftMargin=_MARGIN,
        rightMargin=_MARGIN,
        topMargin=_MARGIN,
        bottomMargin=_MARGIN,
        # Safe metadata only: never the editable run name, an email, a path,
        # or anything credential-bearing.
        title=f"Backtest Report {run.id}",
        author="Grid Backtester",
        subject="Backtest result report",
        creator="Grid Backtester",
    )
    document.build(story, onFirstPage=_page_decoration, onLaterPages=_page_decoration)
    return buffer.getvalue()
