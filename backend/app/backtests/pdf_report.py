"""In-memory PDF backtest report (SPEC Sections 25.4, 32).

The report is assembled entirely from persisted rows -- BacktestRun, its
Dataset, the stored configuration/result_metrics documents, and the
normalized DailyEquity/Trade series. The engine is never invoked, no metric
is recomputed, and no bytes are written to disk: the document is built into
a BytesIO and returned.

Section order is frozen by SPEC 32 and followed exactly:

  1 name, 2 security, 3 data range, 4 cleaning summary, 5 strategy
  parameters, 6 fee/slippage, 7 OHLC-path assumption, 8 core metrics,
  9 both benchmarks, 10 price chart, 11 equity curve, 12 drawdown chart,
  13 first 20 trades, 14 cost summary, 15 risk disclaimer.

Decimal values are carried as Decimal/str end to end; a float appears only
at the final graphics-coordinate boundary inside the chart helpers, never in
rendered metric text.
"""

