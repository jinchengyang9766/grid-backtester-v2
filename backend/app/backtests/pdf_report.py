def _mapping(value: object) -> dict[str, Any]:
    return value if isinstance(value, dict) else {}


def _benchmark_points(metrics: dict[str, Any], key: str) -> list[Decimal]:
    points = _mapping(metrics.get(key)).get("points")
    if not isinstance(points, list):
        return []
    values: list[Decimal] = []
    for point in points:
        parsed = _decimal(_mapping(point).get("equity"))
        if parsed is not None:
            values.append(parsed)
    return values


def _equity_series_rows(label: str, series: dict[str, Any]) -> list[tuple[str, str]]:
    return [
        (f"{label} - Initial Equity", _money(series.get("initial_equity"))),
        (f"{label} - Final Equity", _money(series.get("final_equity"))),
        (f"{label} - Net Profit", _money(series.get("net_profit"))),
        (f"{label} - Total Return", _ratio(series.get("total_return"))),
        (f"{label} - Annualized Return", _ratio(series.get("annualized_return"))),
        (f"{label} - Maximum Drawdown", _ratio(series.get("maximum_drawdown"))),
        (f"{label} - Sharpe Ratio", _quantize(series.get("sharpe_ratio"))),
    ]


