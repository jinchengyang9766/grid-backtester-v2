_cjk_registered = False


def _ensure_fonts() -> None:
    """Register the CJK CID font once per process."""
    global _cjk_registered
    if not _cjk_registered:
        pdfmetrics.registerFont(UnicodeCIDFont(_CJK_FONT))
        _cjk_registered = True


def _needs_cjk(text: str) -> bool:
    return any(ord(char) > 0x2FF for char in text)


# --------------------------------------------------------------------------
# value rendering -- stored strings stay strings; no float is ever involved
# --------------------------------------------------------------------------


def _dash(value: object) -> str:
    """Render a missing value consistently."""
    return "-" if value is None else str(value)


def _yes_no(value: object) -> str:
    if value is None:
        return "-"
    return "Yes" if bool(value) else "No"


def _enabled(value: object) -> str:
    if value is None:
        return "-"
    return "Enabled" if bool(value) else "Disabled"


def _decimal(value: object) -> Decimal | None:
    """Parse a stored decimal string without ever going through float."""
    if value is None or isinstance(value, bool):
        return None
    if isinstance(value, Decimal):
        return value
    if isinstance(value, int):
        return Decimal(value)
    if isinstance(value, str):
        try:
            return Decimal(value)
        except InvalidOperation:
            return None
    return None


def _quantize(value: object, places: int = 6) -> str:
    """Fixed-point rendering of a ratio-style metric; never scientific."""
    parsed = _decimal(value)
    if parsed is None:
        return "-"
    exponent = Decimal(1).scaleb(-places)
    return format(parsed.quantize(exponent), "f")


def _percent(value: object, places: int = 4) -> str:
    """Human-readable percentage label alongside the stored ratio."""
    parsed = _decimal(value)
    if parsed is None:
        return "-"
    exponent = Decimal(1).scaleb(-places)
    return f"{format((parsed * 100).quantize(exponent), 'f')}%"


def _ratio(value: object) -> str:
    if _decimal(value) is None:
        return "-"
    return f"{_quantize(value)}  ({_percent(value)})"


def _money(value: object) -> str:
    """Monetary/price values render exactly as stored (already fixed-point)."""
    parsed = _decimal(value)
    if parsed is None:
        return "-"
    return format(parsed, "f")


def _int_text(value: object) -> str:
    if value is None:
        return "-"
    if isinstance(value, bool):
        return _yes_no(value)
    if isinstance(value, int):
        return str(value)
    return str(value)


def _date_text(value: datetime.date | None) -> str:
    return "-" if value is None else value.isoformat()


def _timestamp_text(value: datetime.datetime | None) -> str:
    if value is None:
        return "-"
    return value.replace(microsecond=0).isoformat()


def _value_config(config: object, *, unit: str = "") -> str:
    """Render a {mode, value} distance/step block readably."""
    if not isinstance(config, dict):
        return _dash(config)
    mode = config.get("mode")
    raw = config.get("value")
    if raw is None:
        return "-"
    if mode == "PERCENT":
        return f"{_money(raw)} ({_percent(raw)}, percent mode)"
    return f"{_money(raw)}{unit} (fixed mode)"


def _commission_text(config: object) -> str:
    if not isinstance(config, dict):
        return _dash(config)
    parts = [
        f"rate {_enabled(config.get('rate_enabled'))} @ {_money(config.get('rate'))}",
        f"minimum {_enabled(config.get('minimum_enabled'))} @ {_money(config.get('minimum'))}",
        f"fixed {_enabled(config.get('fixed_enabled'))} @ {_money(config.get('fixed'))}",
    ]
    return "; ".join(parts)


def _slippage_text(config: object) -> str:
    if not isinstance(config, dict):
        return _dash(config)
    mode = _dash(config.get("mode"))
    if config.get("shared"):
        return f"Shared, {mode} @ {_money(config.get('value'))}"
    return f"Separate, {mode} - buy {_money(config.get('buy'))}, sell {_money(config.get('sell'))}"


