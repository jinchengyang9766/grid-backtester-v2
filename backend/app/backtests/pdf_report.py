class _Styles:
    """Paragraph styles; CJK-capable variants pick the CID font."""

    def __init__(self) -> None:
        sample = getSampleStyleSheet()
        self.title = ParagraphStyle(
            "ReportTitle",
            parent=sample["Title"],
            fontName=_LATIN_BOLD,
            fontSize=18,
            leading=22,
            spaceAfter=2,
        )
        self.title_cjk = ParagraphStyle("ReportTitleCJK", parent=self.title, fontName=_CJK_FONT)
        self.subtitle = ParagraphStyle(
            "ReportSubtitle",
            parent=sample["Normal"],
            fontName=_LATIN_FONT,
            fontSize=9.5,
            leading=12,
            textColor=colors.HexColor("#555555"),
            alignment=TA_CENTER,
            spaceAfter=8,
        )
        self.heading = ParagraphStyle(
            "SectionHeading",
            parent=sample["Heading2"],
            fontName=_LATIN_BOLD,
            fontSize=12,
            leading=15,
            spaceBefore=10,
            spaceAfter=5,
            textColor=_STRATEGY_COLOR,
            # A heading never sits alone at the foot of a page.
            keepWithNext=True,
        )
        self.body = ParagraphStyle(
            "Body", parent=sample["Normal"], fontName=_LATIN_FONT, fontSize=8.5, leading=11
        )
        self.body_cjk = ParagraphStyle("BodyCJK", parent=self.body, fontName=_CJK_FONT)
        self.cell = ParagraphStyle(
            "Cell", parent=sample["Normal"], fontName=_LATIN_FONT, fontSize=8, leading=10
        )
        self.cell_cjk = ParagraphStyle("CellCJK", parent=self.cell, fontName=_CJK_FONT)
        self.cell_head = ParagraphStyle(
            "CellHead",
            parent=self.cell,
            fontName=_LATIN_BOLD,
            fontSize=6.5,
            leading=8,
            textColor=colors.white,
        )
        # Denser style for the wide trade grid, so numeric columns fit on one line.
        self.compact = ParagraphStyle(
            "Compact", parent=self.cell, fontName=_LATIN_FONT, fontSize=6.5, leading=8
        )
        self.compact_cjk = ParagraphStyle("CompactCJK", parent=self.compact, fontName=_CJK_FONT)
        self.note = ParagraphStyle(
            "Note",
            parent=self.body,
            fontSize=8,
            textColor=colors.HexColor("#8a6d3b"),
            spaceBefore=2,
        )
        self.disclaimer = ParagraphStyle(
            "Disclaimer",
            parent=self.body,
            fontSize=8,
            leading=11,
            textColor=colors.HexColor("#444444"),
        )

    def para(self, text: str, latin: ParagraphStyle, cjk: ParagraphStyle) -> Paragraph:
        escaped = text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
        return Paragraph(escaped, cjk if _needs_cjk(text) else latin)

    def cell_para(self, text: str) -> Paragraph:
        return self.para(text, self.cell, self.cell_cjk)

    def body_para(self, text: str) -> Paragraph:
        return self.para(text, self.body, self.body_cjk)

    def compact_para(self, text: str) -> Paragraph:
        return self.para(text, self.compact, self.compact_cjk)


def _key_value_table(styles: _Styles, rows: list[tuple[str, str]]) -> Table:
    """Two-column label/value table that wraps and repeats no header."""
    data = [[styles.cell_para(label), styles.cell_para(value)] for label, value in rows]
    table = Table(data, colWidths=[_CONTENT_WIDTH * 0.34, _CONTENT_WIDTH * 0.66], repeatRows=0)
    table.setStyle(
        TableStyle(
            [
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ("GRID", (0, 0), (-1, -1), 0.25, colors.HexColor("#cccccc")),
                ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#f2f5f9")),
                ("LEFTPADDING", (0, 0), (-1, -1), 5),
                ("RIGHTPADDING", (0, 0), (-1, -1), 5),
                ("TOPPADDING", (0, 0), (-1, -1), 3),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
            ]
        )
    )
    return table


def _grid_table(
    styles: _Styles,
    header: list[str],
    rows: list[list[str]],
    *,
    weights: list[float] | None = None,
) -> Table:
    """Multi-column table whose header row repeats across page breaks.

    ``weights`` gives each column a share of the content width so numeric
    columns stay wide enough to hold a value on one line.
    """
    column_count = len(header)
    shares = weights or [1.0] * column_count
    total = sum(shares)
    widths = [_CONTENT_WIDTH * share / total for share in shares]
    data: list[list[Paragraph]] = [
        [Paragraph(name, styles.cell_head) for name in header],
        *[[styles.compact_para(cell) for cell in row] for row in rows],
    ]
    table = Table(data, colWidths=widths, repeatRows=1)
    table.setStyle(
        TableStyle(
            [
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ("GRID", (0, 0), (-1, -1), 0.25, colors.HexColor("#cccccc")),
                ("BACKGROUND", (0, 0), (-1, 0), _STRATEGY_COLOR),
                ("LEFTPADDING", (0, 0), (-1, -1), 4),
                ("RIGHTPADDING", (0, 0), (-1, -1), 4),
                ("TOPPADDING", (0, 0), (-1, -1), 3),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
                ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f7f9fb")]),
            ]
        )
    )
    return table


# --------------------------------------------------------------------------
# charts -- Decimal becomes float only here, at the coordinate boundary
# --------------------------------------------------------------------------

_CHART_WIDTH = _CONTENT_WIDTH
_CHART_HEIGHT = 175.0
_PLOT_LEFT = 62.0
_PLOT_BOTTOM = 26.0
_PLOT_TOP = 18.0
_PLOT_RIGHT = 8.0


def _chart_bounds() -> tuple[float, float, float, float]:
    left = _PLOT_LEFT
    bottom = _PLOT_BOTTOM
    width = _CHART_WIDTH - _PLOT_LEFT - _PLOT_RIGHT
    height = _CHART_HEIGHT - _PLOT_BOTTOM - _PLOT_TOP
    return left, bottom, width, height


def _line_chart(
    *,
    title: str,
    labels: list[str],
    series: list[tuple[str, list[Decimal], colors.Color, tuple[int, int] | None]],
    reference_lines: list[tuple[str, Decimal, colors.Color]] | None = None,
    faint_lines: list[Decimal] | None = None,
) -> Drawing:
    """A self-contained line chart drawn from ReportLab primitives.

    Handles empty and single-point series safely and keeps every mark inside
    the drawing's bounds, so nothing can spill outside the page frame.
    """
    drawing = Drawing(_CHART_WIDTH, _CHART_HEIGHT)
    left, bottom, width, height = _chart_bounds()

    drawing.add(
        String(
            0, _CHART_HEIGHT - 11, title, fontName=_LATIN_BOLD, fontSize=9.5, fillColor=colors.black
        )
    )

    plotted = [(label, values, color, dashes) for label, values, color, dashes in series if values]
    if not plotted:
        drawing.add(
            String(
                left,
                bottom + height / 2,
                "No series data available.",
                fontName=_LATIN_FONT,
                fontSize=8.5,
                fillColor=_AXIS_COLOR,
            )
        )
        return drawing

    candidates: list[Decimal] = [value for _, values, _, _ in plotted for value in values]
    for _, reference, _ in reference_lines or []:
        candidates.append(reference)
    for reference in faint_lines or []:
        candidates.append(reference)

    low = min(candidates)
    high = max(candidates)
    if low == high:  # flat or single-point series
        pad = abs(low) / 10 if low != 0 else Decimal("1")
        low, high = low - pad, high + pad
    span = high - low

    point_count = max(len(values) for _, values, _, _ in plotted)

    def to_y(value: Decimal) -> float:
        # Decimal -> float only here, purely to obtain a canvas coordinate.
        ratio = (value - low) / span
        return bottom + float(ratio) * height

    def to_x(index: int) -> float:
        if point_count <= 1:
            return left + width / 2
        return left + (index / (point_count - 1)) * width

    # plot frame and horizontal guides
    drawing.add(
        Rect(left, bottom, width, height, strokeColor=_AXIS_COLOR, strokeWidth=0.5, fillColor=None)
    )
    for step in range(1, 4):
        y = bottom + height * step / 4
        drawing.add(Line(left, y, left + width, y, strokeColor=_GRID_COLOR, strokeWidth=0.3))

    # y-axis labels
    for step in range(5):
        value = low + span * Decimal(step) / Decimal(4)
        y = bottom + height * step / 4
        drawing.add(
            String(
                left - 5,
                y - 3,
                format(value.quantize(Decimal("0.0001")), "f"),
                fontName=_LATIN_FONT,
                fontSize=6,
                fillColor=_AXIS_COLOR,
                textAnchor="end",
            )
        )

    # faint reference levels (grid levels) behind the data
    for reference in faint_lines or []:
        y = to_y(reference)
        drawing.add(Line(left, y, left + width, y, strokeColor=_GRID_COLOR, strokeWidth=0.3))

    # named reference levels (baseline / A / C boundaries)
    for label, reference, color in reference_lines or []:
        y = to_y(reference)
        line = Line(left, y, left + width, y, strokeColor=color, strokeWidth=0.6)
        line.strokeDashArray = [3, 2]
        drawing.add(line)
        drawing.add(
            String(
                left + width - 2,
                y + 2,
                label,
                fontName=_LATIN_FONT,
                fontSize=5.5,
                fillColor=color,
                textAnchor="end",
            )
        )

    # data series
    for _, values, color, dashes in plotted:
        if len(values) == 1:
            y = to_y(values[0])
            drawing.add(Line(left, y, left + width, y, strokeColor=color, strokeWidth=1.1))
            continue
        coordinates: list[float] = []
        for index, value in enumerate(values):
            coordinates.extend([to_x(index), to_y(value)])
        poly = PolyLine(coordinates, strokeColor=color, strokeWidth=0.9)
        if dashes is not None:
            poly.strokeDashArray = list(dashes)
        drawing.add(poly)

    # x-axis labels: a handful of evenly spaced dates
    if labels:
        ticks = min(6, len(labels))
        for step in range(ticks):
            index = 0 if ticks == 1 else round(step * (len(labels) - 1) / (ticks - 1))
            drawing.add(
                String(
                    to_x(index),
                    bottom - 10,
                    labels[index],
                    fontName=_LATIN_FONT,
                    fontSize=6,
                    fillColor=_AXIS_COLOR,
                    textAnchor="middle",
                )
            )

    # legend along the bottom
    legend_x = left
    for label, _, color, _ in plotted:
        drawing.add(Line(legend_x, 6, legend_x + 12, 6, strokeColor=color, strokeWidth=1.2))
        drawing.add(
            String(
                legend_x + 15,
                3.5,
                label,
                fontName=_LATIN_FONT,
                fontSize=6.5,
                fillColor=colors.black,
            )
        )
        legend_x += 22 + len(label) * 3.6

    return drawing


def _zero_reference_chart(*, title: str, labels: list[str], values: list[Decimal]) -> Drawing:
    """Drawdown chart: negative values preserved, zero line included."""
    return _line_chart(
        title=title,
        labels=labels,
        series=[("Drawdown", values, _BENCHMARK1_COLOR, None)],
        reference_lines=[("0", Decimal(0), _AXIS_COLOR)] if values else None,
    )


